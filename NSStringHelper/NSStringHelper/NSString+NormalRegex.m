+ (NSString *)yf_stringNilAndNullCheck:(NSString *)string{
    if (string == nil || [string isEqual:[NSNull null]]) {
        return @"";
    }else {
        return string;
    }
}

+ (BOOL)isStringNilOrNullOrEmpty:(NSString *)string{
    if (string == nil || [string isEqual:[NSNull null]] || [string isEqualToString:@""]) {
        return YES;
    }else {
        return NO;
    }
}

- (BOOL)yf_isValidateByRegex:(NSString *)regex{
    NSPredicate *pre = [NSPredicate predicateWithFormat:@"SELF MATCHES %@",regex];
    return [pre evaluateWithObject:self];
}

