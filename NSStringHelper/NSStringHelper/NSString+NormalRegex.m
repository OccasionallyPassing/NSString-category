+ (NSString *)FilterSpaceString:(NSString *)string{
    NSString *newStr;
    //过滤掉字符串前后的空格
    newStr = [string stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]];
    //过滤掉中间的空格
    newStr = [newStr stringByReplacingOccurrencesOfString:@" " withString:@""];
    return newStr;
}

